package kr.ac.sungkyul.beautyline.vo;

public class JinVo {

	private Long userNo;
	private int basic;
	private int white;
	private int ju;
	private int acne;
	
	
	public Long getUserNo() {
		return userNo;
	}
	public void setUserNo(Long userNo) {
		this.userNo = userNo;
	}
	public int getBasic() {
		return basic;
	}
	public void setBasic(int basic) {
		this.basic = basic;
	}
	public int getWhite() {
		return white;
	}
	public void setWhite(int white) {
		this.white = white;
	}
	public int getJu() {
		return ju;
	}
	public void setJu(int ju) {
		this.ju = ju;
	}
	public int getAcne() {
		return acne;
	}
	public void setAcne(int acne) {
		this.acne = acne;
	}
}
